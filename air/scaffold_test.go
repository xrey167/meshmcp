func TestDeviceNameFromHost(t *testing.T) {
	cases := map[string]string{
		"MyLaptop":       "meshmcp-mylaptop",
		"host.local":     "meshmcp-host-local",
		"  Spaces Here ": "meshmcp-spaces-here",
		"":               "meshmcp-gateway",
		"!!!":            "meshmcp-gateway",
		"a_b__c":         "meshmcp-a-b-c",
	}
	for in, want := range cases {
		if got := DeviceNameFromHost(in); got != want {
			t.Errorf("DeviceNameFromHost(%q) = %q, want %q", in, got, want)
		}
	}
}

func TestParseBackendSpec_Stdio(t *testing.T) {
	got, err := ParseBackendSpec("kg=python -m my_server")
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if got.Name != "kg" {
		t.Errorf("name = %q, want kg", got.Name)
	}
	if got.HTTP != "" {
		t.Errorf("HTTP should be empty, got %q", got.HTTP)
	}
	want := []string{"python", "-m", "my_server"}
	if len(got.Stdio) != len(want) {
		t.Fatalf("stdio = %v, want %v", got.Stdio, want)
	}
	for i := range want {
		if got.Stdio[i] != want[i] {
			t.Errorf("stdio[%d] = %q, want %q", i, got.Stdio[i], want[i])
		}
	}
}

func TestParseBackendSpec_HTTP(t *testing.T) {
	for _, addr := range []string{"http://127.0.0.1:3001", "https://svc.local/mcp"} {
		got, err := ParseBackendSpec("web=" + addr)
		if err != nil {
			t.Fatalf("unexpected error: %v", err)
		}
		if got.HTTP != addr {
			t.Errorf("HTTP = %q, want %q", got.HTTP, addr)
		}
		if got.Stdio != nil {
			t.Errorf("stdio should be nil for http, got %v", got.Stdio)
		}
	}
}

func TestParseBackendSpec_Invalid(t *testing.T) {
	for _, spec := range []string{"noequals", "=cmd", "name=", "name=   ", "\x00bad=cmd"} {
		if _, err := ParseBackendSpec(spec); err == nil {
			t.Errorf("ParseBackendSpec(%q) should error", spec)
		}
	}
}

