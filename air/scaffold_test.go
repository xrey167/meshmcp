// TestRenderConfigYAML_PruneAndComment proves the renderer drops zero-value
// noise, keeps the meaningful deny-by-default posture, and frames the file —
// all without changing the schema (it round-trips back to the same map).
func TestRenderConfigYAML_PruneAndComment(t *testing.T) {
	// A marshaled-struct shape with the null/empty noise a struct marshal emits.
	marshaled := []byte(`mesh:
  device_name: gw
  setup_key: ""
  log_level: warn
trace: null
registry: ""
audit_fail_closed: false
audit_log: ./audit.jsonl
backends:
  - name: example
    port: 9101
    session_ttl_seconds: 0
    policy:
      default_allow: false
      rules: []
`)
	out, err := RenderConfigYAML(marshaled)
	if err != nil {
		t.Fatalf("RenderConfigYAML: %v", err)
	}
	s := string(out)

	// Prune assertions apply to the ACTIVE config, not the commented appendix
	// (which deliberately shows a `rules:` example).
	active := s
	if idx := strings.Index(s, "# ----"); idx >= 0 {
		active = s[:idx]
	}
	for _, noisy := range []string{"trace:", "registry:", "audit_fail_closed:", "setup_key:", "session_ttl_seconds:", "rules:"} {
		if strings.Contains(active, noisy) {
			t.Errorf("expected %q to be pruned, got:\n%s", noisy, active)
		}
	}
	// Meaningful values kept.
	for _, keep := range []string{"device_name: gw", "audit_log: ./audit.jsonl", "default_allow: false", "name: example"} {
		if !strings.Contains(s, keep) {
			t.Errorf("expected %q to be kept, got:\n%s", keep, s)
		}
	}
	// Framed and taught.
	if !strings.Contains(s, "generated by") {
		t.Errorf("missing header comment")
	}
	if !strings.Contains(s, "deny-by-default") {
		t.Errorf("missing deny-by-default teaching comment")
	}
	if !strings.Contains(s, "Advanced (commented examples") {
		t.Errorf("missing advanced appendix")
	}

	// Schema fidelity: the pruned document still parses as a mapping.
	var back map[string]any
	if err := yaml.Unmarshal(out, &back); err != nil {
		t.Fatalf("rendered config does not re-parse: %v", err)
	}
	if _, ok := back["backends"]; !ok {
		t.Errorf("backends missing after render")
	}
}
