// Air Handoff is the Continuity seam between agents and devices. It moves an
// inert, expiring Context Capsule to an exact WireGuard key, persists it under
// the verified source identity, and requires an explicit local accept before a
// receiver-selected governed tool can continue the work in a fresh agent.
// It deliberately does not transfer a live session or its CreatorKey.
func cmdAirHandoff(args []string) error {
	if len(args) == 0 {
		return airHandoffUsage()
	}
	switch args[0] {
	case "offer", "send":
		return cmdAirHandoffOffer(args[1:])
	case "receive", "recv":
		return cmdAirHandoffReceive(args[1:])
	case "list", "inbox":
		return cmdAirHandoffList(args[1:])
	case "show":
		return cmdAirHandoffShow(args[1:])
	case "accept":
		return cmdAirHandoffTransition(args[1:], air.HandoffAccepted)
	case "rearm":
		return cmdAirHandoffRearm(args[1:])
	case "archive", "prune":
		return cmdAirHandoffArchive(args[1:])
	case "decline", "reject":
		return cmdAirHandoffTransition(args[1:], air.HandoffDeclined)
	case "continue":
		return cmdAirHandoffContinue(args[1:])
	case "help", "-h", "--help":
		return airHandoffUsage()
	default:
		return fmt.Errorf("air handoff: unknown subcommand %q (want offer | receive | list | show | accept | decline | continue | rearm | archive)", args[0])
	}
}

