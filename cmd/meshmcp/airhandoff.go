func cmdAirHandoffContinue(args []string) error {
	fs := flag.NewFlagSet("air handoff continue", flag.ExitOnError)
	o := meshFlags(fs)
	inboxDir := fs.String("inbox", "", "handoff inbox directory (required)")
	agentKeyFlag := fs.String("agent-key", "", "exact destination agent WireGuard public key (required; pubkey: prefix accepted)")
	tool := fs.String("tool", "", "destination tool selected by the receiver (required; never taken from capsule context)")
	networkTimeout := fs.Duration("timeout", 2*time.Minute, "maximum time to wait for the destination inbox application ACK")
	if err := fs.Parse(args); err != nil {
		return err
	}
	if fs.NArg() != 2 || *inboxDir == "" || *agentKeyFlag == "" || *tool == "" {
		return errors.New("usage: meshmcp air handoff continue --inbox <dir> --agent-key <key> --tool <name> <id> <agent-ip:port>")
	}
	if *networkTimeout <= 0 {
		return errors.New("air handoff continue: --timeout must be greater than zero")
	}
	inbox, err := newHandoffInbox(*inboxDir)
	if err != nil {
		return err
	}
	o.BlockInbound = true
	client, err := startMesh(o, os.Stderr)
	if err != nil {
		return err
	}
	defer stopMesh(client)
	agentKey := strings.TrimPrefix(*agentKeyFlag, "pubkey:")
	if strings.TrimSpace(agentKey) == "" || agentKey != strings.TrimSpace(agentKey) || len(agentKey) > 256 || handoffHasControl(agentKey) {
		return errors.New("air handoff continue: invalid --agent-key")
	}
	if err := verifyHandoffTargetIdentity(client, fs.Arg(1), agentKey); err != nil {
		return fmt.Errorf("air handoff continue: %w", err)
	}
	deliver := func(ctx context.Context, addr string, env steerEnvelope) error {
		return sendSteerEnvelopeToPeer(ctx, client, addr, agentKey, env)
	}
	signalCtx, stop := signal.NotifyContext(context.Background(), shutdownSignals...)
	defer stop()
	deliveryCtx, cancel := context.WithTimeout(signalCtx, *networkTimeout)
	defer cancel()
	rec, err := continueStoredHandoff(deliveryCtx, inbox, fs.Arg(0), fs.Arg(1), agentKey, *tool, time.Now, deliver)
	if err != nil {
		return fmt.Errorf("air handoff continue: %w", err)
	}
	fmt.Println(okLine("continuation delivered %s → %s", rec.Offer.Capsule.ID, fs.Arg(1)) + dim(" · execution remains policy-governed"))
	return nil
}

