type handoffDeliver func(context.Context, string, steerEnvelope) error

func continueStoredHandoff(ctx context.Context, inbox *handoffInbox, id, agentAddr, agentKey, tool string, nowf func() time.Time, deliver handoffDeliver) (air.HandoffRecord, error) {
	if ctx == nil {
		return air.HandoffRecord{}, errors.New("handoff continuation requires a context")
	}
	if err := ctx.Err(); err != nil {
		return air.HandoffRecord{}, err
	}
	if strings.TrimSpace(agentAddr) == "" || agentAddr != strings.TrimSpace(agentAddr) || len(agentAddr) > 512 || handoffHasControl(agentAddr) {
		return air.HandoffRecord{}, errors.New("invalid destination agent address")
	}
	if strings.TrimSpace(agentKey) == "" || agentKey != strings.TrimSpace(agentKey) || len(agentKey) > 256 || handoffHasControl(agentKey) {
		return air.HandoffRecord{}, errors.New("invalid destination agent key")
	}
	if nowf == nil {
		return air.HandoffRecord{}, errors.New("handoff continuation requires a clock")
	}
	if deliver == nil {
		return air.HandoffRecord{}, errors.New("handoff continuation requires a delivery function")
	}
	rec, found, err := inbox.Get(id)
	if err != nil {
		return air.HandoffRecord{}, err
	}
	if !found {
		return air.HandoffRecord{}, errHandoffNotFound
	}
	checkNow := nowf().UTC()
	if checkNow.IsZero() {
		return air.HandoffRecord{}, errors.New("handoff continuation clock returned zero time")
	}
	if rec.EffectiveState(checkNow) != air.HandoffAccepted {
		if rec.EffectiveState(checkNow) == air.HandoffExpired {
			_, _ = inbox.Transition(id, air.HandoffExpired, checkNow, "offer expired")
		}
		return air.HandoffRecord{}, fmt.Errorf("handoff must be accepted and unexpired before continuation (state %s)", rec.EffectiveState(checkNow))
	}
	env, err := handoffContinuationEnvelope(rec, tool)
	if err != nil {
		return air.HandoffRecord{}, err
	}
	claimNow := nowf().UTC()
	if claimNow.IsZero() {
		return air.HandoffRecord{}, errors.New("handoff continuation clock returned zero time")
	}
	claimed, err := inbox.ClaimDelivery(id, agentAddr, agentKey, tool, claimNow)
	if err != nil {
		return air.HandoffRecord{}, fmt.Errorf("claim continuation delivery: %w", err)
	}
	// Use the capsule's absolute expiry as the authority. This covers time
	// spent in preflight, store locking, delivery, and application-ACK wait.
	deliveryCtx, cancel := context.WithDeadline(ctx, claimed.Offer.Capsule.ExpiresAt)
	err = deliver(deliveryCtx, agentAddr, env)
	ctxErr := deliveryCtx.Err()
	acknowledgedAt := nowf().UTC()
	cancel()
	if err != nil {
		return air.HandoffRecord{}, fmt.Errorf("continuation delivery outcome is unknown; handoff remains dispatching (inspect downstream receipts, then use handoff rearm): %w", err)
	}
	if ctxErr != nil {
		return air.HandoffRecord{}, fmt.Errorf("continuation delivery outcome is unknown; handoff remains dispatching (inspect downstream receipts, then use handoff rearm): %w", ctxErr)
	}
	if acknowledgedAt.IsZero() || !acknowledgedAt.Before(claimed.Offer.Capsule.ExpiresAt) {
		return air.HandoffRecord{}, errors.New("continuation reached its expiry boundary with an unknown delivery outcome; handoff remains dispatching")
	}
	// This means the steer was delivered, not that the destination tool
	// succeeded. The capsule ID correlates downstream receipts; the destination
	// must provide its own deduplication if a tool needs exactly-once semantics.
	continued, err := inbox.AcknowledgeDelivery(id, acknowledgedAt)
	if err != nil {
		return air.HandoffRecord{}, fmt.Errorf("destination acknowledged the continuation but its local receipt could not be committed; handoff remains dispatching with an unknown outcome: %w", err)
	}
	return continued, nil
}

func handoffContinuationEnvelope(rec air.HandoffRecord, tool string) (steerEnvelope, error) {
	if rec.State != air.HandoffAccepted {
		return steerEnvelope{}, fmt.Errorf("handoff state %s is not accepted", rec.State)
	}
	if strings.TrimSpace(tool) == "" || tool != strings.TrimSpace(tool) || len(tool) > 256 || handoffHasControl(tool) {
		return steerEnvelope{}, errors.New("destination tool must be explicitly selected")
	}
	payload := struct {
		Handoff struct {
			Capsule     air.ContextCapsule `json:"capsule"`
			ContentHash string             `json:"content_hash"`
			SourcePeer  string             `json:"source_peer"`
			SourceKey   string             `json:"source_key"`
		} `json:"handoff"`
		HandlingHints []string `json:"handling_hints"`
	}{}
	payload.Handoff.Capsule = rec.Offer.Capsule
	payload.Handoff.ContentHash = rec.Offer.ContentHash
	payload.Handoff.SourcePeer = rec.SourcePeer
	payload.Handoff.SourceKey = rec.SourceKey
	// Sender labels remain inside the capsule as untrusted source metadata.
	// These handling hints are explicit advisory data for the destination tool;
	// they are not policy.Filter taint labels and grant no authority.
	payload.HandlingHints = []string{"handoff", "untrusted-context", "handoff:" + rec.Offer.Capsule.Sensitivity}
	b, err := json.Marshal(payload)
	if err != nil {
		return steerEnvelope{}, err
	}
	env := air.Task(tool, b)
	env.ID = rec.Offer.Capsule.ID
	if err := env.Validate(); err != nil {
		return steerEnvelope{}, err
	}
	return env, nil
}

func parseHandoffWork(raw string) (air.WorkRef, error) {
	kindRaw, id, ok := strings.Cut(raw, ":")
	if !ok || strings.TrimSpace(id) == "" || id != strings.TrimSpace(id) || handoffHasControl(id) {
		return air.WorkRef{}, fmt.Errorf("bad --work %q (want agent|session|task|workflow:<id>)", sanitizeCell(raw))
	}
	kind := air.WorkKind(kindRaw)
	switch kind {
	case air.WorkAgent, air.WorkSession, air.WorkTask, air.WorkWorkflow:
	default:
		return air.WorkRef{}, fmt.Errorf("bad --work kind %q (groups cannot receive a target-key-bound handoff)", sanitizeCell(kindRaw))
	}
	return air.WorkRef{Kind: kind, ID: id}, nil
}

func parseHandoffArtifact(raw string) (air.BlobRef, error) {
	name, hash, ok := strings.Cut(raw, "=")
	if !ok || name == "" || name == "." || name == ".." || len(name) > 255 || strings.ContainsAny(name, `/\`) || handoffHasControl(name) {
		return air.BlobRef{}, fmt.Errorf("bad --artifact %q (want base-name=sha256:<64hex>)", sanitizeCell(raw))
	}
	if len(hash) != len("sha256:")+64 || !strings.HasPrefix(hash, "sha256:") {
		return air.BlobRef{}, fmt.Errorf("bad --artifact hash for %q", sanitizeCell(name))
	}
	for _, c := range hash[len("sha256:"):] {
		if !((c >= '0' && c <= '9') || (c >= 'a' && c <= 'f')) {
			return air.BlobRef{}, fmt.Errorf("bad --artifact hash for %q", sanitizeCell(name))
		}
	}
	return air.BlobRef{Name: name, SHA256: hash}, nil
}

func readHandoffCursor(r io.Reader) (json.RawMessage, error) {
	b, err := io.ReadAll(io.LimitReader(r, air.HandoffMaxInlineBytes+1))
	if err != nil {
		return nil, err
	}
	if len(b) == 0 {
		return nil, errors.New("handoff cursor is empty")
	}
	if len(b) > air.HandoffMaxInlineBytes {
		return nil, fmt.Errorf("handoff cursor exceeds %d bytes", air.HandoffMaxInlineBytes)
	}
	if !json.Valid(b) {
		return nil, errors.New("handoff cursor must be valid JSON")
	}
	return json.RawMessage(b), nil
}

func handoffHasControl(s string) bool {
	for _, r := range s {
		if r < 0x20 || r == 0x7f {
			return true
		}
	}
	return false
}

func validHandoffStateFilter(state string) bool {
	switch air.HandoffState(state) {
	case air.HandoffOffered, air.HandoffAccepted, air.HandoffDispatching, air.HandoffDeclined, air.HandoffContinued, air.HandoffExpired:
		return true
	default:
		return false
	}
}

func handoffListGoal(rec air.HandoffRecord) string {
	switch rec.Offer.Capsule.Sensitivity {
	case air.SensitivitySensitive, air.SensitivityRestricted:
		return "[" + rec.Offer.Capsule.Sensitivity + " context — use show to inspect]"
	default:
		return oneLine(rec.Offer.Capsule.Goal)
	}
}

func printHandoffJSON(v any) error {
	b, err := json.MarshalIndent(v, "", "  ")
	if err != nil {
		return err
	}
	fmt.Println(string(b))
	return nil
}

